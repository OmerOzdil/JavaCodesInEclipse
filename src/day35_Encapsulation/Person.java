package day35_Encapsulation;

public class Person {
	
	
	private String name;
	private int age;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		
		// condition
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		//condition
		this.age = age;
	}
	
	


}
