package day4_arithmeticOperator;

public class task13 {

	public static void main(String[] args) {
		
		
		
		int num1, num2, sum;
		
		num1=100;
		num2=90;
		sum=num1+num2;
		System.out.println(num1+ "+" + num2 +"=" +sum);
		
		//Fahrenheit to Celcius
		
		double fah, cel;
		
		cel=99.2;
		fah=9 * cel / 5 + 32;
		System.out.println(cel + " celcius is equal to " + fah + " Fahrenheit");
		
		double mile, km;
		
		mile=85;
		km=mile*1.609344;
		System.out.println(mile + " mile is equal to "+ km +" km.");
		
		
		
		
		
		

	}

}
